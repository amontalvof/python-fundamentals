value = 100
type_value = type(value)
print(value)
print(type_value)

value_str = str(value)
type_value_str = type(value_str)
print(value_str)
print(type_value_str)

value_int = int(value_str)
type_value_int = type(value_int)
print(value_int)
print(type_value_int)

value_float = float(value_str)
type_value_float = type(value_float)
print(value_float)
print(type_value_float)
